function filterByCrescator(list) {
let select = document.getElementById('sort');
let value;

if (select) {
    value = select.value;
}

if (value == 'crescator') {
    list.sort(function(a, b) {
        return a.pret - b.pret
    });
} else {
    list = list;
}

return list;
}

