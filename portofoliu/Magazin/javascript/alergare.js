function initialDisplay() {
    cardInit(alergare);
}

function cardInit(list) {
    let container = document.getElementById('container');
    if (container) {
        container.innerHTML = '';
    }
    list.forEach((produs) => {
        showCard(container, produs);
    });
}

function showCard(container, produs) {
    if (container) {
        let card = cardInfo(produs);
        if (card) {
            container.appendChild(card);
        }
    }
}

function cardInfo(produs) {

    let card = document.createElement('div');
    card.className = "imagine-produs-biciclete";
    container.appendChild(card)

    let img = document.createElement('img');
    img.className = "img";
    img.src = produs.poza;
    card.appendChild(img)

    let numeProdus = document.createElement("p");
    numeProdus.className = "numeProdus";
    numeProdus.innerHTML = produs.brand;
    card.appendChild(numeProdus)

    let informatie = document.createElement('p');
    informatie.className = "informatie";
    informatie.innerHTML = "Incaltaminte alergare" + " " + produs.numeProdus;
    card.appendChild(informatie);

    let pret = document.createElement('p');
    pret.className = "pret";
    pret.innerHTML = produs.pret + " RON";
    card.appendChild(pret);

    let cos = document.createElement("div");
    cos.className = "cos";
    card.appendChild(cos);

    let i = document.createElement('i');
    i.className = "fa fa-cart-arrow-down";
    cos.appendChild(i);

    let adaugaCos = document.createElement('a');
    adaugaCos.innerHTML = "adauga in cos";
    adaugaCos.className = "adaugaCos";
    cos.appendChild(adaugaCos);
}

